package sec01;

public class CharToCode {

	public static void main(String[] args) {
		char ch = 'A';
		int intCode = 'B';
		System.out.printf("%c = %d \n ", ch, intCode);
		
		char characteC = 'a';
		int charCode = (int)characteC;
		System.out.printf("%c = %d \n ", characteC, charCode);
		System.out.printf("%c = %d \n ", characteC, charCode);

	}
}
